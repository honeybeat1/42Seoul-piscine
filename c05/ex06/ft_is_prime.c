int		ft_is_prime(int nb)
{
	long sqr;

	sqr = 2;
	if (nb <= 1)
		return (0);
	while (sqr * sqr <= nb)
	{
		if (nb % sqr == 0)
			return (0);
		sqr++;
	}
	return (1);
}
