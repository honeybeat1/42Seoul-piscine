int		ft_find_next_prime(int nb)
{
	long sqr;

	sqr = 2;
	if (nb <= 1)
		return (2);
	while (sqr * sqr <= nb)
	{
		if (nb % sqr == 0)
		{
			nb++;
			sqr = 2;
			continue;
		}
		sqr++;
	}
	return (nb);
}
